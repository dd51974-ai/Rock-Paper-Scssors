numbers = [] # Charge number
while True:
    input_number = int(input("数を入力して下さい(終了はdone): "))
    total_amount = sum(numbers.append())
    average_amount = sum(numbers.append())/len(numbers.append())
    if input_number < 0:
        total_amount, average_amount
        if input_number == "done":
            total_amount, average_amount
            print("合計: ", total_amount)
            print("平均: ",)
            break
    elif not input_number:
        print("まず数字を入力して下さい")
    elif not input_number and input_number == "done":
        print("数字が一度も入力されません")
    else:
        print("数を入力して下さい")
    break